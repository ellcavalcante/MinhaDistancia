extension DesignSystem {

    enum Colors {
        static let primary: UIColor = .init(hex: "#FAFAFA")
        static let secondary: UIColor = .init(hex: "#5CAC22")
        static let tertiary: UIColor = .init(hex: "#EE9494")
        static let accent: UIColor = .init(hex: "#F1FFE7")
        static let background: UIColor = .init(hex: "#FFFFFF")
    }
}
